    <p style="text-indent:2em">80、中标人无正当理由不与招标人订立合同的，会被取消中标资格吗？</p>
    <p style="text-indent:2em">答： 会被取消。</p>
    <p style="text-indent:2em">81、招标人和中标人如何订立书面合同？</p>
    <p style="text-indent:2em">答： 合同的标的、价款、质量、履行期限等主要条款必须与招标文件和中标人的投标文件内容一致。</p>
    <p ><img src="../../static/img/tb040.jpg" width="300"/></p>
    <p ><img src="../../static/img/tb041.jpg" width="300"/></p>
    <p style="text-indent:2em">82、评标结果公示应包括哪些内容？</p>
    <p style="text-indent:2em">答： 包括中标候选人名称及排序、投标报价、资质等级、质量目标、履约期限、项目经理、业绩情况等信息。</p>
    <p style="text-indent:2em">83、投标人对招标文件、开标和评审结果未提出异议，可以直接投诉吗？</p>
    <p style="text-indent:2em">答： 不可以。</p>
    <p style="text-indent:2em">84、异议受理主体是谁？</p>
    <p style="text-indent:2em">答： 招标人或经授权的招标代理机构。</p>
    <p ><img src="../../static/img/tb042.jpg" width="300"/></p>
    <p style="text-indent:2em">85、潜在投标人或者其他利害关系人对招标文件有异议，在什么时间提出？</p>
    <p style="text-indent:2em">答： 在投标截止时间10日前提出。</p>
    <p style="text-indent:2em">86、投标人或者其他利害关系人对评审结果有异议，在什么时间提出？</p>
    <p style="text-indent:2em">答： 在公示期间提出。</p>
    <p style="text-indent:2em">87、招标人应该在什么时间与中标单位签订合同？</p>
    <p style="text-indent:2em">答： 应该自中标通知书发出之日起30日内与中标单位签订合同。</p>
    <p ><img src="../../static/img/tb043.jpg" width="300"/></p>
    <p style="text-indent:2em">88、招标人在异议未答复前，需要暂停招标投标活动吗？</p>
    <p style="text-indent:2em">答： 需要暂停招标投标活动。</p>
    <p style="text-indent:2em">89、我市政府投资工程项目，能否将其拆分成50万元以下而不招标？</p>
    <p style="text-indent:2em">答： 不能。这是化整为零规避招标。</p>
    <p ><img src="../../static/img/tb044.jpg" width="300"/></p>
    <p ><img src="../../static/img/tb045.jpg" width="300"/></p>
    <p style="text-indent:2em">90、出让或者出租资格、资质证书供他人投标的，会被追究责任吗？</p>
    <p style="text-indent:2em">答： 会被追究责任。依照法律、行政法规给予行政处罚；构成犯罪的，依法追究刑事责任。</p>
    <p style="text-indent:2em">91、投标人相互串通投标或者与招标人串通投标的，中标有效吗？</p>
    <p style="text-indent:2em">答： 中标无效。并依法承担相应法律责任。</p>
    <p style="text-indent:2em">92、投标人以他人名义投标，中标有效吗？</p>
    <p style="text-indent:2em">答： 中标无效。并依法承担相应法律责任。</p>
    <p style="text-indent:2em">93、投标人的投标报价明显低于其个别成本，且未按要求做出合理的说明并提供相关的证明材料，评标委员会能否作废标处理？</p>
    <p style="text-indent:2em">答： 能作废标处理。</p>
    <p style="text-indent:2em">94、国家工作人员利用职务便利非法干涉招标投标活动会受处分吗？</p>
    <p style="text-indent:2em">答： 依规给予行政处分；构成犯罪的，依法追究刑事责任。</p>
    <p ><img src="../../static/img/tb046.jpg" width="300"/></p>
    <p style="text-indent:2em">95、因招标人对中标人不满意，可否不发中标通知书或者不与中标人订立合同？</p>
    <p style="text-indent:2em">答： 不可以。</p>
    <p ><img src="../../static/img/tb047.jpg" width="300"/></p>
    <p ><img src="../../static/img/tb048.jpg" width="300"/></p>
    <p style="text-indent:2em">96、中标人可以在中标后向他人转让中标项目，或者将中标项目肢解后分别向他人转让吗？</p>
    <p style="text-indent:2em">答： 不可以。</p>
    <p style="text-indent:2em">97、招标人无正当理由不发中标通知书或者不与中标人订立合同要承担赔偿责任吗？</p>
    <p style="text-indent:2em">答： 要承担。</p>
    <p style="text-indent:2em">98、中标人按照合同约定或者经招标人同意，可以将中标项目的部分非主体、非关键性工作分包给他人完成吗？</p>
    <p style="text-indent:2em">答： 可以。但接受分包的人应当具备相应的资格条件，并不得再次分包。</p>
    <p style="text-indent:2em">99、招标人应该保留中标单位的投标文件多少年？</p>
    <p style="text-indent:2em">答： 应该保留10年以上。</p>
    <p style="text-indent:2em">100、招标人需要保留未中标单位的投标文件吗？</p>
    <p style="text-indent:2em">答： 需要。</p>
